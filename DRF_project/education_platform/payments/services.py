import stripe
from django.conf import settings

stripe.api_key = settings.STRIPE_SECRET_KEY


def create_stripe_product(name: str, description: str = ""):
    return stripe.Product.create(name=name, description=description)


def create_stripe_price(product_id: str, amount_usd: float):
    # Stripe требует сумму в центах!
    return stripe.Price.create(
        product=product_id,
        unit_amount=int(amount_usd * 100),  # ← умножаем на 100
        currency="usd",
    )


def create_checkout_session(price_id: str, success_url: str, cancel_url: str):
    return stripe.checkout.Session.create(
        payment_method_types=["card"],
        line_items=[{"price": price_id, "quantity": 1}],
        mode="payment",
        success_url=success_url,
        cancel_url=cancel_url,
    )
